namespace Zenith.NET;

/// <summary>
/// Specifies how polygons are rasterized.
/// </summary>
public enum FillMode
{
    /// <summary>
    /// Polygons are filled completely.
    /// </summary>
    Solid,

    /// <summary>
    /// Polygons are outlined in a "wireframe" style.
    /// </summary>
    Wireframe
}
